var animal = "Elefante";

switch(animal){
    case "Cavalo":
        console.log(animal);
        console.log('É um animal dócil e muito veloz !');
        break;

    case "Cachorro":
        console.log(animal);
        console.log('Dependendo da raça pode ser dócil ou agressivo!');
        break;

    case "Elefante":
        console.log(animal);
        console.log('É um animal pesado e muito grande!');
        break;

    default:
        console.log('Não reconhecemos esse animal!')
}